import { Link, useLocation } from 'react-router-dom';
import { cn } from '@/lib/utils';
import { useSettingsStore } from '@/store/settingsStore';

const MobileNav = () => {
  const location = useLocation();
  const { menuItems } = useSettingsStore();

  // Map some default icons or fallback to Menu
  const getIcon = (label: string) => {
    const lower = label.toLowerCase();
    if (lower.includes('home')) return HomeIcon;
    if (lower.includes('movie')) return FilmIcon;
    if (lower.includes('series') || lower.includes('tv')) return TvIcon;
    if (lower.includes('download')) return DownloadIcon;
    if (lower.includes('admin') || lower.includes('setting')) return SettingsIcon;
    return MenuIcon; // Default
  };

  // Filter visible items and take first 4-5 to fit mobile
  const navLinks = (menuItems || []).filter(item => item.visible).slice(0, 5);

  return (
    <nav className="md:hidden fixed bottom-0 left-0 right-0 z-50 bg-background/95 backdrop-blur-md border-t border-white/5 pb-safe">
      <div className="flex items-center justify-around h-16">
        {navLinks.map((link) => {
          const Icon = getIcon(link.label);
          const isActive = location.pathname === link.path;

          return (
            <Link
              key={link.id}
              to={link.path}
              className={cn(
                "flex flex-col items-center justify-center w-full h-full space-y-1",
                isActive ? "text-primary" : "text-muted-foreground hover:text-white"
              )}
            >
              <Icon className="w-5 h-5" />
              <span className="text-[10px] font-medium">{link.label}</span>
            </Link>
          );
        })}
      </div>
    </nav>
  );
};

